static int
handle_number(const char *src, const size_t srcl, size_t *traversed, char *result, const size_t resultl, size_t *written)
{
  size_t i = *traversed;
  size_t j = *written;
  int ret = 0;

  i += 2;
  uint64_t data = 0;
  size_t trav;
  if (isdigit(src[i])) {
    data = get_fromdec(src+i,MIN(DECODE_ENTITIES_MAX_DIGITS,srcl-i),&trav);
  } else {
    i++;
    data = get_fromhex(src+i,MIN(DECODE_ENTITIES_MAX_XDIGITS,srcl-i),&trav);
  }
  i += trav;
  uchar ended = (i < srcl && src[i] == ';');

  data = enc32utf8(data);

  if (write_utf8(data,result,&trav,resultl-j) == -1) {
    ret = -1;
    goto END;
  }

  j += trav;
  if (ended)
    i++;

  END: ;
  *traversed = i;
  *written = j;
  return ret;
}

static inline uchar
find_special_in(struct htmlcode const **first, const char *name, const size_t len)
{
  uchar found = 0;
  struct htmlcode const *f = *first;
  for (; f-html_special_codes != LENGTH(html_special_codes) && f->name.s == len; f++) {
    if (memcmp(f->name.b,name,len) == 0) {
      found = 1;
      break;
    }
  }
  *first = f;
  return found;
}

static struct htmlcode const*
find_special(const char *name, const size_t maxnamel, size_t *length)
{
  size_t len = 1;
  struct htmlcode const* first = html_special_codes;
  do {
    len++;
    if (len > 6)
      continue;

    //this handles special escapes that don't have to be closed with ';'
    if (find_special_in(&first,name,len))
      goto FOUND;

  } while (len < maxnamel && isalpha(name[len]) && len <= DECODE_ENTITIES_MAX_NAME);

  *length = len;
  return NULL;

  FOUND: ;
  *length = len;
  return first;
}

static struct htmlcode const*
find_normal(const char *name, const size_t namel)
{
  struct htmlcode const *first = html_codes;
  for (; first-html_codes != LENGTH(html_codes); first++)
    if (first->name.s == namel && memcmp(first->name.b,name,namel) == 0)
      return first;

  return NULL;
}

static struct htmlcode const*
find_name(const char *name, const size_t maxnamel, size_t *len)
{
  *len = 1;
  struct htmlcode const *first = find_special(name,maxnamel,len);
  if (first)
    goto END;

  if (*len >= maxnamel || name[*len] != ';')
    goto END;

  first = find_normal(name,*len);
  *len = 0;

  END: ;
  return first;
}

static int
handle_name(const char *src, const size_t srcl, size_t *traversed, char *result, const size_t resultl, size_t *written, uchar *ignore, bool no_nbsp)
{
  size_t i = *traversed;
  size_t j = *written;
  int ret = 0;

  i++;
  uchar ended=0;
  size_t len;

  struct htmlcode const *first = find_name(src+i,srcl-i,&len);
  if (!first) {
    *ignore = 1;
    return 0;
  }

  if (len == 0 || (i+len < srcl && src[i+len] == ';'))
    ended = 1;

  const char *val = first->val;
  size_t vall = strlen(val);
  if (j+vall >= resultl) {
    ret = -1;
    goto END;
  }

  if (no_nbsp && vall == 2 && ((uchar)val[0]) == 0xc2 && ((uchar)val[1]) == 0xa0) { // \u00a0
    result[j] = ' ';
    j += 1;
  } else {
    memcpy(result+j,val,vall);
    j += vall;
  }

  i += first->name.s;
  if (ended)
    i++;

  END: ;
  *traversed = i;
  *written = j;
  return ret;
}

int
reliq_decode_entity(const char *src, const size_t srcl, size_t *traversed, char *result, const size_t resultl, size_t *written, bool no_nbsp)
{
  int ret = 0;
  size_t i=0,j=0;

  if (src[0] == '&' && 2 < srcl) {
    if (isalpha(src[1]) && isalpha(src[2])) {
      uchar ignore = 0;
      ret = handle_name(src,srcl,&i,result,resultl,&j,&ignore,no_nbsp);
      if (ignore)
        goto IGNORE;
    } else if (src[1] == '#' && (isdigit(src[2]) || ((src[2] == 'x' || src[2] == 'X') && 3 < srcl && isxdigit(src[3])))) {
      ret = handle_number(src,srcl,&i,result,resultl,&j);
    } else
      goto IGNORE;
  } else {
    IGNORE: ;
    uchar c = src[i++];
    result[j++] = c;
  }

  *traversed = i;
  *written = j;
  return ret;
}

void
reliq_decode_entities_sink(const char *src, const size_t srcl, SINK *out, bool no_nbsp)
{
  char buf[BUF_SIZE];
  size_t buf_used = 0;

  for (size_t i = 0; i < srcl;) {
    if (unlikely(BUF_SIZE-buf_used < RELIQ_DECODE_ENTITY_MAXSIZE)) {
      sink_write(out,buf,buf_used);
      buf_used = 0;
    }

    size_t traversed,written;
    reliq_decode_entity(src+i,srcl-i,&traversed,buf+buf_used,BUF_SIZE-buf_used,&written,no_nbsp);
    i += traversed;
    buf_used += written;
  }
  if (buf_used)
    sink_write(out,buf,buf_used);
}

